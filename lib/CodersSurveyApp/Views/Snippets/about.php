	<h3>
		The Idea
	</h3>
	<p>
		We have been asking ourselves how the common web developer works. So we have been poking around for a survey. But no valid data has been available. So we just did this little poll you see here. The approach is »Curious, not serious«.
	</p>
	<h3>
		Why participate?
	</h3>
	<p>
		Get to know how "state of the art" you are. Check out what other coders are up to. But most important: Help other developers to understand your needs better so that they can build better products for you.
	</p>
	<h3>
		How to answer the Questions
	</h3>
	<p>
		Filling out the form takes about 5 minutes. All questions are optional. Just leave out the questions that don't matter to you. Please don't SPAM the survey.
	</p>
	<h3>
		Privacy
	</h3>
	<p>
		Your entered data is handled anonymously. We use anonymized Google Analytics – no IPs should be stored. And there are Facebook-, Google+-, Twitter-Buttons – they all do their data mining.
	</p>
	<h3>
		The Coders´ Survey on GitHub
	</h3>
	<p>
		We have built a simple little survey CMS for the »Coders´ Survey«. It's based on the <a href="http://www.slimframework.com/">Slim</a> framework and features a creepy web based backend to enter your questions. Feel free to rip, mix and burn it. Fork this on <a href="https://github.com/fortrabbit/coders-survey">GitHub</a>. 
	</p>
